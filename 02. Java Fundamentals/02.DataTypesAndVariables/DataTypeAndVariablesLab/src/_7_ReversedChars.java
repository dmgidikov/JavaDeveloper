import java.util.Scanner;

public class _7_ReversedChars {

    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);

        char a = sc.next().charAt(0);
        char b = sc.next().charAt(0);
        char c = sc.next().charAt(0);

        System.out.print(c);
        System.out.print(" ");
        System.out.print(b);
        System.out.print(" ");
        System.out.print(a);
    }
}
